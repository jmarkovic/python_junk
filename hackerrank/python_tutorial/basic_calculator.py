first_number = float(input())
second_number = float(input())

print('{0:.2f}'.format(first_number + second_number))
print('{0:.2f}'.format(first_number - second_number))
print('{0:.2f}'.format(first_number * second_number))
print('{0:.2f}'.format(first_number / second_number))
print('{0:.2f}'.format(first_number // second_number))